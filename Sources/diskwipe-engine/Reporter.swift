import Foundation

/// Writes per-disk markdown report and appends a row to INDEX.md,
/// matching the existing ~/disk-reports layout.
enum Reporter {
    static var dir: String { Paths.reportsDir }
    static var indexPath: String { (dir as NSString).appendingPathComponent("INDEX.md") }

    struct Outcome {
        let disk: DiskInfo
        let mode: String          // "full" or "quick"
        let pre: SmartSnapshot
        let post: SmartSnapshot
        let eraseSummary: String
        let verifySummary: String
        let longTestSummary: String
        let verdict: String       // emoji + words
        let date: String
    }

    static func write(_ o: Outcome) {
        try? FileManager.default.createDirectory(atPath: dir, withIntermediateDirectories: true)
        ensureIndexHeader()
        writeDetail(o)
        appendIndexRow(o)
    }

    private static func ensureIndexHeader() {
        if !FileManager.default.fileExists(atPath: indexPath) {
            let header = """
            # Disk Test Reports — Index

            | Date | Model | Serial | Capacity | Interface | Power-On (h) | Wear | SMART | Erase | Verify | Verdict | Report |
            |------|-------|--------|----------|-----------|--------------|------|-------|-------|--------|---------|--------|

            """
            try? header.write(toFile: indexPath, atomically: true, encoding: .utf8)
        }
    }

    private static func detailPath(_ serial: String) -> String {
        let safe = serial.isEmpty ? "unknown-\(Int(Date().timeIntervalSince1970))" : serial
        return (dir as NSString).appendingPathComponent("\(safe).md")
    }

    private static func writeDetail(_ o: Outcome) {
        func b(_ v: Int?) -> String { v.map(String.init) ?? "—" }
        let wear = o.post.wearValueNormalized.map { "VALUE \($0)" } ?? "—"
        let md = """
        # Disk Report — \(o.disk.model)

        **Serial:** \(o.disk.serial)
        **Tested:** \(o.date)
        **Mode:** \(o.mode) erase
        **Verdict:** \(o.verdict)

        ## Identity

        | Field | Value |
        |-------|-------|
        | Model | \(o.disk.model) |
        | Serial | \(o.disk.serial) |
        | Firmware | \(o.disk.firmware) |
        | Capacity | \(o.disk.sizeBytes) bytes (\(String(format: "%.1f", o.disk.sizeGB)) GB) |
        | Type | \(o.disk.isSSD ? "SSD" : "HDD") |
        | Interface | \(o.disk.busProtocol) |
        | Block size | \(o.disk.blockSize) B |

        ## Health (before → after)

        | Attribute | Before | After |
        |-----------|--------|-------|
        | SMART health | \(o.pre.healthPassed.map { $0 ? "PASSED" : "FAILED" } ?? "—") | \(o.post.healthPassed.map { $0 ? "PASSED" : "FAILED" } ?? "—") |
        | Power-On Hours | \(b(o.pre.powerOnHours)) | \(b(o.post.powerOnHours)) |
        | Power Cycles | \(b(o.pre.powerCycles)) | \(b(o.post.powerCycles)) |
        | Wear (norm.) | \(o.pre.wearValueNormalized.map { "VALUE \($0)" } ?? "—") | \(wear) |
        | Reallocated | \(b(o.pre.reallocated)) | \(b(o.post.reallocated)) |
        | Pending | \(b(o.pre.pending)) | \(b(o.post.pending)) |
        | Offline Uncorrectable | \(b(o.pre.offlineUncorrectable)) | \(b(o.post.offlineUncorrectable)) |
        | Reported Uncorrect | \(b(o.pre.reportedUncorrect)) | \(b(o.post.reportedUncorrect)) |
        | UDMA CRC Errors | \(b(o.pre.crcErrors)) | \(b(o.post.crcErrors)) |
        | Command Timeout | \(b(o.pre.commandTimeout)) | \(b(o.post.commandTimeout)) |
        | Temperature °C | \(b(o.pre.temperatureC)) | \(b(o.post.temperatureC)) |

        ## Operations

        | Step | Result |
        |------|--------|
        | Erase (\(o.mode)) | \(o.eraseSummary) |
        | Verify-read | \(o.verifySummary) |
        | SMART long self-test | \(o.longTestSummary) |
        | Final partition map | Empty GPT (no filesystem) |

        _Generated by diskwipe-engine._
        """
        try? md.write(toFile: detailPath(o.disk.serial), atomically: true, encoding: .utf8)
    }

    private static func appendIndexRow(_ o: Outcome) {
        let wear = o.post.wearValueNormalized.map { "VALUE \($0)" } ?? "—"
        let serialFile = o.disk.serial.isEmpty ? "unknown" : o.disk.serial
        let row = "| \(o.date) | \(o.disk.model) | \(o.disk.serial) | "
            + "\(String(format: "%.1f", o.disk.sizeGB)) GB | \(o.disk.busProtocol) | "
            + "\(o.post.powerOnHours.map(String.init) ?? "—") | \(wear) | "
            + "\(o.post.healthPassed.map { $0 ? "PASSED" : "FAILED" } ?? "—") | "
            + "\(o.eraseSummary) | \(o.verifySummary) | \(o.verdict) | [link](\(serialFile).md) |\n"
        if let fh = FileHandle(forWritingAtPath: indexPath) {
            fh.seekToEndOfFile()
            fh.write(row.data(using: .utf8)!)
            try? fh.close()
        }
    }
}
